public class Example {

   public static void main(String args[]) {

      int i = Math.abs(-1);
      System.out.println(i);

      double d = Math.abs(-2.22);
      System.out.println(d);

      float f = Math.abs(-3.33f);
      System.out.println(f);

      long l = Math.abs(-4L);
      System.out.println(l);
   }
}


