import java.util.PriorityQueue;

public class Example {

   public static void main(String[] args) {

      // create priority queue
      PriorityQueue<Integer> pq = new PriorityQueue<>();

      // add elements
      pq.add(10);
      pq.add(20);
      pq.add(30);

      // print out queue
      System.out.println(pq);
   }
}


